export function parseStationCoordinates() {
  return STATION_COORDINATE_SOURCE.split('\n')
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const [left, right] = line.split('/');
      if (!right) {
        return null;
      }
      const lhs = left.trim();
      const rhs = right.trim();
      const match = lhs.match(/^([A-Za-z0-9,-]+)\s+(.*)$/);
      const codes = match ? match[1].split(',').map((code) => code.trim()) : [];
      const name = (match ? match[2] : lhs).trim();
      const [xStr = '0', yStr = '0'] = rhs.split(',').map((part) => part.trim());
      const x = Number(xStr);
      const y = Number(yStr);
      return {
        name,
        codes,
        position: { x, y }
      };
    })
    .filter(Boolean);
}
